package Practico.POO.Ejercicio4.Entidad;

public class Rectangulo {
    private int altura;
    private int base;



    //Constructores
    public Rectangulo() {
    }

    public Rectangulo(int altura, int ancho) {
        this.altura = altura;
        this.base = ancho;
    }
    // Getters y Setters

    public int getAltura() {
        return altura;
    }

    public int getBase() {
        return base;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }

    public void setBase(int base) {
        this.base = base;
    } 

    
    



    
}
